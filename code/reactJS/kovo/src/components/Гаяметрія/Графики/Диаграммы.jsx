const DiagramList = () => {
  const [sortConfig, setSortConfig] = useState({ key: 'synonymsEng', direction: 'ascending' });

  const sortedData = [...diagramData].sort((a, b) => {
    if (a[sortConfig.key] < b[sortConfig.key]) {
      return sortConfig.direction === 'ascending' ? -1 : 1;
    }
    if (a[sortConfig.key] > b[sortConfig.key]) {
      return sortConfig.direction === 'ascending' ? 1 : -1;
    }
    return 0;
  });

  const requestSort = key => {
    let direction = 'ascending';
    if (sortConfig.key === key && sortConfig.direction === 'ascending') {
      direction = 'descending';
    }
    setSortConfig({ key, direction });
  };

  return (
    <div>
      <h1>Типы диаграмм</h1>
      <table>
        <thead>
          <tr>
            <th onClick={() => requestSort('synonymsEng')}>Синонимы</th>
            <th onClick={() => requestSort('synonymsRus')}>Синонимы</th>
            <th onClick={() => requestSort('advantages')}>Преимущества</th>
            <th onClick={() => requestSort('disadvantages')}>Недостатки</th>
            <th onClick={() => requestSort('application')}>Применение</th>
            <th onClick={() => requestSort('purpose')}>Назначение</th>
            <th onClick={() => requestSort('dataType')}>Тип данных</th>
          </tr>
        </thead>
        <tbody>
          {sortedData.map((diagram, index) => (
            <tr key={index}>
              <td>{diagram.synonymsEng.join(', ')}</td>
              <td>{diagram.synonymsRus.join(', ')}</td>
              <td>{diagram.advantages.join(', ')}</td>
              <td>{diagram.disadvantages.join(', ')}</td>
              <td>{diagram.application.join(', ')}</td>
              <td>{diagram.purpose.join(', ')}</td>
              <td>{diagram.dataType.join(', ')}</td>
            </tr>
          ))}
        </tbody>
      </table>
    </div>
  );
};

export default DiagramList;
