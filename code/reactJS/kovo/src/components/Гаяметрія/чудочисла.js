
function найтиЧудесныеЧисла(n) {
    const tolerance = 1e-10; // точность вычислений
    const maxIterations = 1000; // максимальное число итераций

    function f(x) {
        return Math.pow(x, n) - Math.pow(x, n - 1) - 1;
    }

    function df(x) {
        return n * Math.pow(x, n - 1) - (n - 1) * Math.pow(x, n - 2);
    }

    let x = 1.5; // начальное предположение
    let iteration = 0;

    while (iteration < maxIterations) {
        const fx = f(x);
        const dfx = df(x);

        if (Math.abs(fx) < tolerance) {
            return x;
        }

        x -= fx / dfx;
        iteration++;
    }

    throw new Error('Не удалось найти решение');
}

