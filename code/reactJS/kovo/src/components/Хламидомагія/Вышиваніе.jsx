const sortData = (data, key, ascending) => {
  return [...data].sort((a, b) => {
    if (a[key] < b[key]) return ascending ? -1 : 1;
    if (a[key] > b[key]) return ascending ? 1 : -1;
    return 0;
  });
};

const EmbroideryTechniques = () => {
  const [data, setData] = useState(embroideryData);
  const [sortConfig, setSortConfig] = useState({ key: '', ascending: true });

  const handleSort = (key) => {
    let ascending = true;
    if (sortConfig.key === key && sortConfig.ascending) {
      ascending = false;
    }
    setData(sortData(data, key, ascending));
    setSortConfig({ key, ascending });
  };

  return (
    <div>
      <h1>Техники вышивки</h1>
      <table>
        <thead>
          <tr>
            <th onClick={() => handleSort('name')}>Название</th>
            <th onClick={() => handleSort('description')}>Описание</th>
            <th onClick={() => handleSort('advantages')}>Преимущества</th>
            <th onClick={() => handleSort('disadvantages')}>Недостатки</th>
            <th onClick={() => handleSort('application')}>Применение</th>
            <th onClick={() => handleSort('availability')}>Доступность по материалам и инструментам</th>
            <th onClick={() => handleSort('fabricTypes')}>Виды тканей</th>
            <th onClick={() => handleSort('transferMethods')}>Способы переноса рисунка</th>
            <th onClick={() => handleSort('threadTypes')}>Виды нитей</th>
            <th onClick={() => handleSort('complexity')}>Сложность</th>
            <th onClick={() => handleSort('stitchSecuring')}>Способ закрепления стежков</th>
          </tr>
        </thead>
        <tbody>
          {data.map((item, index) => (
            <tr key={index}>
              <td>{item.name}</td>
              <td>{item.description}</td>
              <td>{item.advantages}</td>
              <td>{item.disadvantages}</td>
              <td>{item.application}</td>
              <td>{item.availability}</td>
              <td>{item.fabricTypes}</td>
              <td>{item.transferMethods}</td>
              <td>{item.threadTypes}</td>
              <td>{item.complexity}</td>
              <td>{item.stitchSecuring}</td>
            </tr>
          ))}
        </tbody>
      </table>
    </div>
  );
};

export default EmbroideryTechniques;
